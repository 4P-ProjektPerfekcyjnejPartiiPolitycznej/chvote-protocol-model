package ch.ge.ve.protocol.model;

import com.fasterxml.jackson.annotation.JsonIgnore;
import java.io.Serializable;
import java.math.BigInteger;

/**
 * The abstract model interface representing a cyclic group
 */
public interface CyclicGroup extends Serializable {

  /**
   * @return this cyclic group's modulus
   */
  @JsonIgnore
  BigInteger getModulus();

  /**
   * @return this cyclic group's order
   */
  @JsonIgnore
  BigInteger getOrder();
}
