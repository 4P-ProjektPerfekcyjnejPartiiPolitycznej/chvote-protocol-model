package ch.ge.ve.protocol.model;

import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.annotation.JsonProperty;
import com.google.common.collect.ImmutableList;
import java.math.BigInteger;
import java.util.List;
import java.util.Objects;

/**
 * Model class for a decryption proof such as those generated by algorithm 5.50
 */
@SuppressWarnings("unused")
public final class DecryptionProof {
  private final List<BigInteger> t;
  private final BigInteger       s;

  @JsonCreator
  public DecryptionProof(@JsonProperty("t") List<BigInteger> t, @JsonProperty("s") BigInteger s) {
    this.t = ImmutableList.copyOf(t);
    this.s = s;
  }

  public List<BigInteger> getT() {
    return t;
  }

  public BigInteger getS() {
    return s;
  }

  @Override
  public boolean equals(Object o) {
    if (this == o) {
      return true;
    }
    if (o == null || getClass() != o.getClass()) {
      return false;
    }
    DecryptionProof that = (DecryptionProof) o;
    return Objects.equals(t, that.t) && Objects.equals(s, that.s);
  }

  @Override
  public int hashCode() {
    return Objects.hash(t, s);
  }
}
